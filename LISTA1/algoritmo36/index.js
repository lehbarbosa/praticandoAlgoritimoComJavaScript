// Ler dois números inteiros e imprimir a soma. Antes do resultado, 
// deverá aparecer a mensagem: Soma. 
const num1 = parseInt(prompt('Digite um número: '))
const num2 = parseInt(prompt('Digite um outro número: '))

const sum = num1 + num2

alert(`A soma dos números:  ${num1} e ${num2} = ${sum}`)
